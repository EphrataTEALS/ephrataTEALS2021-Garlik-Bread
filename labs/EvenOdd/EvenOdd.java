/**
 * Prints out if number is even or odd to the console.
 * 
 * @author Jacob
 */

 package labs.EvenOdd;

 public class EvenOdd {

    public static void main(String[] args) {
        int user = 0;

        if ((user % 2) == 0) {
            System.out.println(user+ " is Even.");
         } else {
         System.out.println(user+ " is Odd.");
         }


    }
 }